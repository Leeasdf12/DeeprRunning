"""# > 문자열 분할 문제(split)

카카오톡의 대화 정보는 다음과 같이 저장된다.
"2023년 1월 2일 오후 9:52, 유연휘 : 일단"
위 문장을, 날짜, 시간, 이름, 내용별로 분할해보자.

"""

kakaotalk = "2023년 1월 2일 오후 9:52, 유연휘 : 일단"

kakaotalk = kakaotalk.replace(",","")
kt = kakaotalk.split()

print("년 = %s" % kt[0])
print("월 = %s" % kt[1])
print("일 = %s" % kt[2])
print("시간 = %s" % kt[3:5])
print("이름 = %s" % kt[5])
print("내용 = %s" % kt[7])