"""# > 문자열 인덱싱 문제
문자열 인덱싱 또는 슬라이싱을 사용하여 년 월일 날씨를 출력하시오.

*   text = '20230601sunnyy'
*   year = '2023'
*   day = '01'
*   weather = 'sunny'
"""

text = '20230601sunny'
year = text[:4]
day = text[4:8]
weather = text[8:]

print("text = %s"% text)
print("year = %s"% year)
print("day = %s"% day)
print("weather = %s"% weather)

"""# > 전화번호 형식 바꾸기 문제
010-1234-5678의 형식으로 저장된 전화번호를 '-'을 제거해 01012345678의 형식으로 바꿔보시오.

*   문자열 내장 함수를 사용하면 해결할 수 있습니다.
*   문자열 슬라이싱을 이용해도 상관없습니다.


"""

num = '010-1234-5678'

result = num.replace("-","")
print(result)

"""# > 제목글 변환 문제
사용자에게 영어로된 문자열을 입력받고, 첫 문자만 대문자로 변경하시오.
입력 : i am a boy
출력 : I am a boy
"""

str = input("문자열 입력 : ")
